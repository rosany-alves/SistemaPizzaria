    private void jButtonPesquisarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarClienteActionPerformed
       try {
            int cod = Integer.parseInt(jTextFieldCodigo.getText());
            ClienteModel obj = new ClienteModel();
            ClienteDAO dao = new ClienteDAO();
            obj = dao.consultaPorCodigo(cod);
            
       jTextFieldCodigo.setText(String.valueOf(obj.getCodigo()));
       jTextFieldNome.setText(obj.getNome());
       jFormattedTextFieldCpf.setText(obj.getCpf());
       jTextFieldEmail.setText(obj.getEmail());
       jTextFieldEndereco.setText(obj.getEndereco());
       jTextFieldNumero.setText(String.valueOf(obj.getNumero()));
       jTextFieldRua.setText(obj.getRua());
       jFormattedTextFieldCep.setText(obj.getCep());
       jTextFieldBairro.setText(obj.getBairro());
       jTextFieldCidade.setText(obj.getCidade());
       jTextFieldUf.setText(obj.getUf());
       jFormattedTextFieldTelefone.setText(obj.getTelefone());
       jFormattedTextFieldCelular.setText(obj.getCelular());
       jFormattedTextFieldData.setText(obj.getData());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Erro ao mostrar os dados na tela cadastro."+e);
     
        }
    }//GEN-LAST:event_jButtonPesquisarClienteActionPerformed

