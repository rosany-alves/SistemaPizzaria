    public void listar(){
        ClienteDAO dao = new ClienteDAO();
        List<ClienteModel> lista = dao.listarClientes();
        DefaultTableModel dados = (DefaultTableModel)jTablePesquisar.getModel();
        dados.setNumRows(0) ;
        
        for(ClienteModel c : lista){
            dados.addRow(new Object[]{
            c.getCodigo(),
            c.getNome(),
            c.getCpf(),
            c.getEmail(),
            c.getEndereco(),
            c.getNumero(),
            c.getRua(),
            c.getCep(),
            c.getBairro(),
            c.getCidade(),
            c.getUf(),
            c.getTelefone(),
            c.getCelular(),
            c.getData()
            });
        }
        
    }

