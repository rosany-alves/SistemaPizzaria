    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        String nome = "%"+jTextFieldPesquisarNome.getText()+"%";
        EntregadorDAO dao = new EntregadorDAO();
        List<EntregadorModel> lista = dao.buscaEntregadorNome(nome);
        DefaultTableModel dados = (DefaultTableModel)jTablePesquisar.getModel();
        dados.setNumRows(0) ;
        
        for(EntregadorModel c : lista){
            dados.addRow(new Object[]{
            c.getCodigo(),
            c.getNome(),
            c.getCelular(),
            c.getData()
            });
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

