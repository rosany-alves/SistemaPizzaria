    public void listar(){
        FornecedorDAO dao = new FornecedorDAO();
        List<FornecedorModel> lista = dao.listarFornecedor();
        DefaultTableModel dados = (DefaultTableModel)jTablePesquisarFornecedor.getModel();
        dados.setNumRows(0) ;
        
        for(FornecedorModel c : lista){
            dados.addRow(new Object[]{
            c.getId(),
            c.getNome(),
            c.getCnpj(),
            c.getTelefone(),
            c.getCelular(),
            c.getCep(),
            c.getEndereco(),
            c.getNumero(),
            c.getComplemento(),
            c.getBairro(),
            c.getCidade(),
            c.getEstado()
            });
        }
        
    }

