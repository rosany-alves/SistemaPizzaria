    private void jButtonPesquisarNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarNomeActionPerformed
        String nome = "%"+jTextFieldPesquisarNome.getText()+"%";
        FornecedorDAO dao = new FornecedorDAO();
        List<FornecedorModel> lista = dao.buscaFornecedorNome(nome);
        DefaultTableModel dados = (DefaultTableModel)jTablePesquisarFornecedor.getModel();
        dados.setNumRows(0) ;
        
        for(FornecedorModel c : lista){
            dados.addRow(new Object[]{
            c.getId(),
            c.getNome(),
            c.getCnpj(),
            c.getTelefone(),
            c.getCelular(),
            c.getCep(),
            c.getEndereco(),
            c.getNumero(),
            c.getComplemento(),
            c.getBairro(),
            c.getCidade(),
            c.getEstado()
            });
        }
    }//GEN-LAST:event_jButtonPesquisarNomeActionPerformed

